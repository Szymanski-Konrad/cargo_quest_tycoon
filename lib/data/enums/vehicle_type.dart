enum VehicleType {
  /// Ciężarówka do ładunków ogólnych
  /// General cargo truck
  generalCargo,

  /// Chłodnia - do przewozu towarów w kontrolowanej temperaturze
  /// Refrigerated truck
  refrigerated,

  /// Cysterna do przewozu płynów
  /// Tanker truck for liquids
  tanker,

  /// Wywrotka do materiałów sypkich
  /// Dump truck for bulk materials
  dumpTruck,

  /// Platforma do ładunków wielkogabarytowych
  /// Flatbed truck for oversized cargo
  flatbed,

  /// Kontenerowiec do przewozu kontenerów
  /// Container truck
  container,

  /// Do materiałów niebezpiecznych
  /// Hazardous materials truck
  hazmat,
}
